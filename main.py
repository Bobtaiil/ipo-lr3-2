n = int(input("Введите первое число")) #запрос на ввод двух чисел
k = int(input("Введите второе число")) #запрос на ввод двух чисел
if n > k: #Проверка условий
    print(k) #вывод наименьшего числа при выполнении соотв. словия
elif k > n: #Проверка условий
    print(n) #вывод наименьшего числа при выполнении соотв. словия
else: #Проверка условий
    print('Оба числа равны') #вывод результата сравнения 2 чисел при выполнении соотв условия